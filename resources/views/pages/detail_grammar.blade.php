   <ul>
    <li><strong>Phủ định:</strong> S + didn’t + V_inf + O</li>
   </ul>

   <p>Eg: I didn’t go to school yesterday. (Ngày hôm qua tôi đã không đi học)</p>

   <ul>
    <li><strong>Nghi vấn:</strong> Did + S + V_inf + O?</li>
   </ul>

