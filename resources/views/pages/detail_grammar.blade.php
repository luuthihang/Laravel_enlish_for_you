    <li>Diễn tả hành động đang diễn ra nhưng không nhất thiết xảy ra tại thời điểm nói. (E.g: I am looking for a job)</li>
    <li>Diễn tả 1 sự than phiền với hành động lặp đi lặp lại nhiều gây khó chịu, bực mình. Trong trường hợp này, câu thường có trạng từ tần suất "always". (E.g : He is always borrowing our books and then he doesn’t remember.)</li>
    <li>Dùng để diễn tả một hành động sắp xảy ra trong tương lai theo kế hoạch đã định trước (E.g: I am flying to Thailand tomorrow.)</li>
   </ul>
   </td>
  </tr>
  <tr height="134">
   <td height="134" width="342">➤ <strong><em>Dấu hiệu nhận biết:</em></strong>
   <ul>
    <li>Trong câu có những từ: Now, right now, at present, at the moment,</li>
    <li>Trong các câu trước đó là một câu chỉ mệnh lệnh: Look!, Watch! Be quite!, ...</li>
   </ul>
   </td>
  </tr>
  <tr height="109">
   <td height="109" width="342">➤ <em><strong>Lưu ý quan trọng cần biết:</strong></em><br>
