   </ul>

   <p>Eg: My car has been stolen./ Chiếc xe của tôi đã bị lấy cắp.</p>

   <ul>
    <li>Diễn tả hành động bắt đầu ở quá khứ và đang tiếp tục ở hiện tại</li>
   </ul>

