   <ul>
    <li>Just, recently, lately: gần đây, vừa mới</li>
    <li>Already : đã….rồi , before: đã từng</li>
    <li>Not….yet: chưa</li>
    <li>Never, ever</li>
    <li>Since, for</li>
    <li>So far = until now = up to now: cho đến bây giờ</li>
    <li>So sánh nhất</li>
   </ul>
