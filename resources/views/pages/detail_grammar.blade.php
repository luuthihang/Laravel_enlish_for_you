  <tr height="62">
   <td height="62" style="background-color: rgb(255, 255, 102);" width="510">
   <h3 style="text-align: center;">Past Perfect</h3>
   </td>
  </tr>
  <tr height="209">
   <td height="209" width="510">➤ <strong><em>Công thức:</em></strong>
   <ul>
    <li><strong>Khẳng định:</strong> S + had + V3/ed + O</li>
   </ul>
   Eg: The storm had attacked my city before last night. (Cơn bão đã tấn công thành phố trước tối hôm qua)

   <ul>
    <li><strong>Phủ định:</strong> S + had + not + V3/ed + O</li>
   </ul>
   Eg: The storm hadn’t attacked my city before last night. (Cơn bão đã chưa tấn công thành phố trước tối hôm qua)

   <ul>
    <li><strong>Nghi vấn:</strong> Had + S + V3/ed + O?</li>
   </ul>
   Eg: Had you left home befor 2015? (Có phải bạn đã bỏ nhà đi trước năm 2015?)</td>
  </tr>
  <tr height="247">
   <td height="247" width="510">➤ <strong><em>Cách dùng:</em></strong>
   <ul>
    <li>Diễn tả hành động đã hoàn thành trước một thời điểm trong quá khứ</li>
   </ul>
