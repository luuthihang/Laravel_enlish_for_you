   </ul>
   Eg: I am a worker.

   <ul>
    <li>Phủ định: S + am/is/are + not + O</li>
   </ul>
   Eg: I am not a worker.

   <ul>
    <li>Nghi vấn: Am/is/are + S + O?</li>
   </ul>
