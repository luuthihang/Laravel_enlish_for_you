   <ul>
    <li><strong>Khẳng định:</strong> S + V(s/es) + O</li>
   </ul>

   <p>Eg: I come home at 8 o’clock.</p>

   <ul>
    <li><strong>Phủ định:</strong> S + do not /does not + V_inf</li>
   </ul>

