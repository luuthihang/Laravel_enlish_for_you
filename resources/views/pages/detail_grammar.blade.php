  <tr height="247">
   <td height="247" width="342">➤ <em><strong>Công thức:</strong></em>
   <ul>
    <li><strong>Khẳng định:</strong> S + am/is/are + V_ing</li>
   </ul>
   Eg: He is watching TV now. (Anh ấy đang xem ti vi)

   <ul>
    <li><strong>Phủ định:</strong> S + am/is/are + not + V_ing</li>
   </ul>
   Eg: He is not doing his homework now. (Anh ấy không đang làm bài tập)

   <ul>
    <li><strong>Nghi vấn:</strong> Am/Is/Are + S + V_ing?</li>
