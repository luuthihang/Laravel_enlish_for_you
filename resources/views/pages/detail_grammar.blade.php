  </tr>
  <tr height="164">
   <td colspan="2" height="164" width="635">➤ <strong><em>Cách sử dụng:</em></strong>
   <ul>
    <li>Diễn tả một sự thật hiển nhiên, một chân lý. (E.g: The sun rises in the East and sets in the West.)</li>
    <li>Diễn tả 1 thói quen, một hành động thường xảy ra ở hiện tại. (E.g: Nam gets up early every morning.)</li>
    <li>Miêu tả khả năng, năng khiếu của 1 người. (E.g: She plays football very well.)</li>
   </ul>
   </td>
  </tr>
  <tr height="134">
   <td colspan="2" height="134" width="635">➤ <strong><em>Dấu hiệu nhận biết thì hiện tại đơn:</em></strong>
   <ul>
    <li>Trong câu có xuất hiện từ sau: every (every day, every week, every month, ...)</li>
    <li>Các trạng từ tần suất xuất hiện trong thì hiện tại đơn:  Always , usually, often, sometimes, seldom, rarely, hardly, never.</li>
   </ul>
   </td>
  </tr>
