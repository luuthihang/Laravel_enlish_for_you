   <ul>
    <li>Các động từ thông thường: => động từ + s (ví dụ: reads)</li>
    <li>Động từ kết thúc bằng <strong><em><span style="background-color:#FFFF00;">s, ss, sh, x, o</span></em></strong> => động từ + <em><strong>es</strong></em> (ví dụ: washes)</li>
    <li>Động từ kết thúc bằng phụ âm + <strong><em>y</em></strong> => <strong><em>đổi thành i + es</em></strong> (ví dụ: fly => flies)</li>
    <li>Động từ kết thúc bằng nguyên âm + <strong><em>y</em></strong> => động từ + <em><strong>s</strong></em> (ví dụ: plays)</li>
    <li>Động từ kết thúc bằng <strong><em><span style="background-color:#FFFF00;">f, fe</span></em></strong> => đổi <strong><em>f, fe</em></strong> thành <em><strong>ves</strong></em> (ví dụ: dwarf => dwarves)</li>
   </ul>
   </td>
  </tr>
